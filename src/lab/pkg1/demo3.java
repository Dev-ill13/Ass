    public boolean checkNull() {
        if (txtUsername.getText().length() == 0) {
            JOptionPane.showMessageDialog(this, "Tên không để trống");
            requestFocus();
            return false;
        } else if (txtPassword.getText().length() == 0) {
            JOptionPane.showMessageDialog(this, "Mật khẩu không để trống");
            requestFocus();
            return false;
        } else if (txtConfirm.getText().length() == 0) {
            JOptionPane.showMessageDialog(this, "Confirm không để trống");
            requestFocus();
            return false;
        } else {
            if (txtPassword.getText().equals(txtConfirm.getText())) {
                JOptionPane.showMessageDialog(this, "Đăng nhập thành công");

            } else {
                JOptionPane.showMessageDialog(this, "Mời người dùng nhập lại confirm");

            }
            return true;
        }
    }

