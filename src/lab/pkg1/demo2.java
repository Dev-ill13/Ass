    public boolean checkNull() {
        if (txtNum1.getText().length() == 0) {
            lblLoiNum1.setText("Chưa nhập ô đầu");
            requestFocus();
            return false;
        } else if (txtNum2.getText().length() == 0) {
            lblLoiNum2.setText("Chưa nhập ô 2");
            return false;
        } else {
            num1 = Double.parseDouble(txtNum1.getText());
            num2 = Double.parseDouble(txtNum2.getText());
            return true;
        }
    }

